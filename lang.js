const translations = window.translations;

/* =========================
   LANGUAGE FUNCTIONS
========================= */

function getSavedLanguage() {
  return localStorage.getItem("siteLanguage") || "ru";
}

function getDictionary(lang) {
  return translations[lang] || translations.ru;
}

function applyTranslations(lang) {
  const dict = getDictionary(lang);

  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");

    if (key && dict[key] !== undefined) {
      element.textContent = dict[key];
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.getAttribute("data-i18n-placeholder");

    if (key && dict[key] !== undefined) {
      element.setAttribute("placeholder", dict[key]);
    }
  });

  document.querySelectorAll("[data-i18n-title]").forEach((element) => {
    const key = element.getAttribute("data-i18n-title");

    if (key && dict[key] !== undefined) {
      element.setAttribute("title", dict[key]);
    }
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    const key = element.getAttribute("data-i18n-aria-label");

    if (key && dict[key] !== undefined) {
      element.setAttribute("aria-label", dict[key]);
    }
  });
}

function setActiveLanguageButton(lang) {
  document.querySelectorAll(".lang-btn").forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === lang);
  });
}

function setLanguage(lang) {
  const selectedLang = translations[lang] ? lang : "ru";

  localStorage.setItem("siteLanguage", selectedLang);
  applyTranslations(selectedLang);
  setActiveLanguageButton(selectedLang);

  window.dispatchEvent(
    new CustomEvent("site-language-changed", {
      detail: { lang: selectedLang },
    }),
  );
}

document.addEventListener("DOMContentLoaded", () => {
  const savedLang = getSavedLanguage();

  setLanguage(savedLang);

  document.querySelectorAll(".lang-btn").forEach((button) => {
    button.addEventListener("click", () => {
      const selectedLang = button.dataset.lang;

      if (!selectedLang) {
        return;
      }

      setLanguage(selectedLang);
    });
  });
});
